package model1.mylibrary.seat;

public class SeatDTO {

	private String seatId;
	private String seatNm;
	private String seatLocation;
	private String seatStatus;
	private String seatStatusNM;
	
	
	public String getSeatId() {
		return seatId;
	}
	public void setSeatId(String seatId) {
		this.seatId = seatId;
	}
	public String getSeatNm() {
		return seatNm;
	}
	public void setSeatNm(String seatNm) {
		this.seatNm = seatNm;
	}
	public String getSeatLocation() {
		return seatLocation;
	}
	public void setSeatLocation(String seatLocation) {
		this.seatLocation = seatLocation;
	}
	public String getSeatStatus() {
		return seatStatus;
	}
	public void setSeatStatus(String seatStatus) {
		this.seatStatus = seatStatus;
	}
	public String getSeatStatusNM() {
		return seatStatusNM;
	}
	public void setSeatStatusNM(String seatStatusNM) {
		this.seatStatusNM = seatStatusNM;
	}
	
	
	
	
}
