package model1.mylibrary.book;

public class BookDTO {

	private String bookCode;
	private String bookTitle;
	private String bookAuthor;
	private String bookGenre;
	private String bookStatus;
	private String bookStatusNm;
	private String publisher;
	private java.sql.Date publishDate;
	
	public String getBookCode() {
		return bookCode;
	}
	public void setBookCode(String bookCode) {
		this.bookCode = bookCode;
	}
	public String getBookTitle() {
		return bookTitle;
	}
	public void setBookTitle(String bookTitle) {
		this.bookTitle = bookTitle;
	}
	public String getBookAuthor() {
		return bookAuthor;
	}
	public void setBookAuthor(String bookAuthor) {
		this.bookAuthor = bookAuthor;
	}
	public String getBookGenre() {
		return bookGenre;
	}
	public void setBookGenre(String bookGenre) {
		this.bookGenre = bookGenre;
	}
	public String getBookStatus() {
		return bookStatus;
	}
	public void setBookStatus(String bookStatus) {
		this.bookStatus = bookStatus;
	}
	public String getBookStatusNm() {
		return bookStatusNm;
	}
	public void setBookStatusNm(String bookStatusNm) {
		this.bookStatusNm = bookStatusNm;
	}
	public String getPublisher() {
		return publisher;
	}
	public void setPublisher(String publisher) {
		this.publisher = publisher;
	}
	public java.sql.Date getPublishDate() {
		return publishDate;
	}
	public void setPublishDate(java.sql.Date publishDate) {
		this.publishDate = publishDate;
	}
	
	
	
	
}
